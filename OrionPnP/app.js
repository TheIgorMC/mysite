/* ── HELPERS ── */
function t(key) {
  return (i18n[state.lang] && i18n[state.lang][key]) || i18n.en[key] || key;
}

async function loadLocales() {
  try {
    const res = await fetch("/api/locales", { headers: { Accept: "application/json" } });
    if (!res.ok) return;
    const remote = await res.json();
    ["en", "it", "fr", "es"].forEach((lang) => {
      if (remote && remote[lang] && typeof remote[lang] === "object") {
        i18n[lang] = { ...(i18n[lang] || {}), ...remote[lang] };
      }
    });
  } catch (_) {
    // Keep bundled defaults if locales endpoint is unavailable.
  }
}

function pickText(value) {
  if (!value) return "";
  if (typeof value === "string") return value;
  return value[state.lang] || value.en || Object.values(value)[0] || "";
}

function applyTranslations() {
  document.documentElement.lang = state.lang;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    el.textContent = t(key);
  });
}

/* ── ROUTER ── */
function navigate(pageId) {
  if (!document.getElementById("page-" + pageId)) return;
  state.page = pageId;

  document.querySelectorAll(".page").forEach((p) => p.classList.remove("active"));
  document.getElementById("page-" + pageId).classList.add("active");

  document.querySelectorAll(".nav-link").forEach((a) => {
    a.classList.toggle("active", a.getAttribute("data-page") === pageId);
  });

  window.scrollTo({ top: 0, behavior: "instant" });
  triggerReveal();
}

/* ── NAV SETUP ── */
function setupNav() {
  document.querySelectorAll("[data-page]").forEach((el) => {
    el.addEventListener("click", (e) => {
      e.preventDefault();
      navigate(el.getAttribute("data-page"));
    });
  });

  document.querySelectorAll("[data-nav]").forEach((el) => {
    el.addEventListener("click", () => navigate(el.getAttribute("data-nav")));
  });

  const burger = document.getElementById("navBurger");
  const links  = document.getElementById("navLinks");
  if (burger && links) {
    burger.addEventListener("click", () => {
      const open = links.classList.toggle("open");
      burger.setAttribute("aria-expanded", String(open));
    });
    links.addEventListener("click", () => {
      links.classList.remove("open");
      burger.setAttribute("aria-expanded", "false");
    });
  }

  window.addEventListener("scroll", () => {
    document.getElementById("navbar").classList.toggle("scrolled", window.scrollY > 30);
  });
}

/* ── LANGUAGE ── */
