/* ── FORM ── */
function setupForm() {
  const form   = document.getElementById("interestForm");
  const status = document.getElementById("formStatus");
  if (!form) return;

  form.addEventListener("submit", async (e) => {
    e.preventDefault();
    status.textContent = "";
    status.style.color = "";

    const data     = new FormData(form);
    const intent   = (document.querySelector(".intent-card input:checked") || {}).value || "stay_posted";
    const rawPrice = String(data.get("max_price") || "").trim();
    const maxPrice = rawPrice !== "" ? Number(rawPrice) : null;

    const payload = {
      name:      String(data.get("name")     || "").trim(),
      email:     String(data.get("email")    || "").trim(),
      interest:  String(data.get("interest") || "").trim(),
      message:   String(data.get("message")  || "").trim(),
      intent,
      max_price: maxPrice,
      language:  state.lang
    };

    if (!payload.name || !payload.email || !payload.interest) {
      status.textContent = t("submitErr");
      status.style.color = "var(--danger)";
      return;
    }

    try {
      const res = await fetch("/api/register", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(payload)
      });
      if (!res.ok) throw new Error("bad status");
      form.reset();
      status.textContent = t("submitOk");
      status.style.color = "var(--success)";
    } catch (_) {
      status.textContent = t("submitErr");
      status.style.color = "var(--danger)";
    }
  });
}

/* ── REVEAL ANIMATION ── */
function triggerReveal() {
  const io = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("visible");
        io.unobserve(entry.target);
      }
    });
  }, { threshold: 0.1 });

  const activePage = document.querySelector(".page.active");
  if (!activePage) return;
  activePage.querySelectorAll(".reveal, .reveal-child").forEach((el) => {
    if (!el.classList.contains("visible")) io.observe(el);
  });
}

/* ── VIDEO FALLBACK ── */
function setupVideo() {
  const v = document.getElementById("bgVideo");
  if (!v) return;
  v.addEventListener("error", () => { v.style.display = "none"; });
}




/* -- THEME TOGGLE -- */
function setupTheme() {
  const saved = localStorage.getItem('orion_theme');
  const prefersDark = window.matchMedia('(prefers-color-scheme: dark)').matches;
  const isDark = saved ? saved === 'dark' : prefersDark;
  applyTheme(isDark ? 'dark' : 'light');
  document.getElementById('themeToggle').addEventListener('click', () => {
    const cur = document.documentElement.dataset.theme || 'dark';
    applyTheme(cur === 'dark' ? 'light' : 'dark');
  });
}
function applyTheme(theme) {
  document.documentElement.dataset.theme = theme;
  localStorage.setItem('orion_theme', theme);
  const btn = document.getElementById('themeToggle');
  if (btn) btn.textContent = theme === 'dark' ? '☀️' : '🌙';
}

/* ── BOOT ── */
async function init() {
  setupTheme();
  setupNav();
  await loadLocales();
  setupLanguage();
  setupVideo();
  setupIntentCards();
  setupForm();
  triggerReveal();

  try {
    await loadContent();
  } catch (_) {
    const box = document.getElementById("updatesList");
    if (box) box.textContent = "Unable to load project content.";
  }
}

init();


